package com.example.rotundalaboratoryusermanual;



import android.app.Activity;
import android.util.DisplayMetrics;

public class InitDisplayParams {

	public static void initDisplayMetrics(Activity activity){
		
		DisplayParams.displayMetrics=new DisplayMetrics();
		activity.getWindowManager().getDefaultDisplay().getMetrics(DisplayParams.displayMetrics);
		
	}
	
}
